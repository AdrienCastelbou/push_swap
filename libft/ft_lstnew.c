#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*elem;

	elem = (t_list *)malloc(sizeof(t_list));
	if (!(elem))
		return (NULL);
	elem->content = (void *)content;
	elem->next = NULL;
	return (elem);
}
